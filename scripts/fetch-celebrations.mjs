// Fetches BambooHR birthday and anniversary iCal feeds and writes
// structured JSON data to src/data/celebrations.generated.js.
// Runs as `prebuild` so the bundle always contains fresh-at-deploy data.

import { writeFileSync, mkdirSync, existsSync } from "node:fs";
import { dirname } from "node:path";
import { fileURLToPath } from "node:url";
import { resolve } from "node:path";
import ical from "node-ical";

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT_PATH = resolve(__dirname, "../src/data/celebrations.generated.js");

const FEEDS = {
  birthdays:
    "https://switch.bamboohr.com/feeds/feed.php?id=6ec1a93186a6e5b6b414228f65d1c687",
  anniversaries:
    "https://switch.bamboohr.com/feeds/feed.php?id=b99008846115d7cf731e2c60c390b606",
};

function toMMDD(date) {
  const m = String(date.getMonth() + 1).padStart(2, "0");
  const d = String(date.getDate()).padStart(2, "0");
  return `${m}-${d}`;
}

// Birthday summaries come in several forms:
//   "Jane Doe - Birthday"
//   "Jane Doe's Birthday"
//   "Jane Doe: Birthday"
function parseBirthdaySummary(summary) {
  const name = summary
    .replace(/\s*[-–—:]\s*Birthday\s*$/i, "")
    .replace(/['’]s\s+Birthday\s*$/i, "")
    .replace(/\s+Birthday\s*$/i, "")
    .trim();
  return { name };
}

// Anniversary summaries from BambooHR vary. Common forms:
//   "John Smith - 5 Year Anniversary"
//   "John Smith's 5 Year Anniversary"
//   "John Smith: 5th Anniversary"
// We extract the name and the number of years.
function parseAnniversarySummary(summary) {
  const s = summary.trim();
  const yearMatch = s.match(/(\d+)\s*(?:year|yr|st|nd|rd|th)/i);
  const years = yearMatch ? parseInt(yearMatch[1], 10) : null;

  const name = s
    .replace(/[-:]?\s*\d+\s*(?:year|yr|st|nd|rd|th)[\w\s]*anniversary/i, "")
    .replace(/['’]s\s*$/i, "")
    .replace(/\s+anniversary\s*$/i, "")
    .trim();

  return { name, years };
}

async function fetchFeed(url) {
  const res = await fetch(url, {
    headers: { "User-Agent": "SCCCMarketingDatabase/build" },
  });
  if (!res.ok) throw new Error(`Feed ${url} returned ${res.status}`);
  return res.text();
}

function extractEvents(icalText) {
  const data = ical.parseICS(icalText);
  const events = [];
  for (const key of Object.keys(data)) {
    const e = data[key];
    if (e.type !== "VEVENT" || !e.start || !e.summary) continue;
    events.push({ summary: e.summary, start: new Date(e.start) });
  }
  return events;
}

async function main() {
  console.log("Fetching BambooHR celebration feeds…");

  const [birthdaysText, anniversariesText] = await Promise.all([
    fetchFeed(FEEDS.birthdays),
    fetchFeed(FEEDS.anniversaries),
  ]);

  const birthdayEvents = extractEvents(birthdaysText);
  const anniversaryEvents = extractEvents(anniversariesText);

  // Deduplicate by name (iCal may contain multiple occurrences per person).
  const birthdaysByName = new Map();
  for (const ev of birthdayEvents) {
    const { name } = parseBirthdaySummary(ev.summary);
    if (!name) continue;
    if (!birthdaysByName.has(name)) {
      birthdaysByName.set(name, { name, date: toMMDD(ev.start) });
    }
  }

  const anniversariesByName = new Map();
  for (const ev of anniversaryEvents) {
    const { name, years } = parseAnniversarySummary(ev.summary);
    if (!name) continue;
    // Calculate startYear from years-at-event.
    const startYear = years ? ev.start.getFullYear() - years : null;
    const existing = anniversariesByName.get(name);
    // Keep the earliest startYear we see (most accurate).
    if (!existing || (startYear && startYear < existing.startYear)) {
      anniversariesByName.set(name, {
        name,
        date: toMMDD(ev.start),
        startYear: startYear ?? new Date().getFullYear() - (years ?? 0),
      });
    }
  }

  const birthdays = [...birthdaysByName.values()].sort((a, b) =>
    a.date.localeCompare(b.date)
  );
  const anniversaries = [...anniversariesByName.values()].sort((a, b) =>
    a.date.localeCompare(b.date)
  );

  const fileBody = `// Generated by scripts/fetch-celebrations.mjs — do not edit by hand.
// Source: BambooHR calendar feeds. Re-runs on every build.

export const birthdays = ${JSON.stringify(birthdays, null, 2)};

export const anniversaries = ${JSON.stringify(anniversaries, null, 2)};

export const generatedAt = ${JSON.stringify(new Date().toISOString())};
`;

  if (!existsSync(dirname(OUT_PATH))) mkdirSync(dirname(OUT_PATH), { recursive: true });
  writeFileSync(OUT_PATH, fileBody, "utf8");

  console.log(
    `Wrote ${birthdays.length} birthdays and ${anniversaries.length} anniversaries to ${OUT_PATH}`
  );
}

main().catch((err) => {
  console.error("fetch-celebrations failed:", err.message);
  // Write an empty file so the build can still proceed.
  const fallback = `// Build-time fetch failed: ${err.message.replace(/\*\//g, "")}
export const birthdays = [];
export const anniversaries = [];
export const generatedAt = null;
`;
  if (!existsSync(dirname(OUT_PATH))) mkdirSync(dirname(OUT_PATH), { recursive: true });
  writeFileSync(OUT_PATH, fallback, "utf8");
  process.exit(0);
});
